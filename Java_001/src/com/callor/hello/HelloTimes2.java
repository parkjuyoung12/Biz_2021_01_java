package com.callor.hello;

// class : java 코드의 가장 기본 구조
// 파일명 : 클래스명.java
//		파일명과 클래스명은 같아야 한다
// 클래스명 : 첫글자 대문자
//		소문자,숫자, _ 를 조합하여 이름 짓기
public class HelloTimes2 {

	// main() method : 메인 메소드
	// 키워드() : method라고 한다.
	// main() method는 java 코드가 제일먼저
	// 실행되는 곳이다
	// 진입점 method라고 하기도 한다.
	public static void main(String[] args) {
		// 실행되는 코드가 있는 곳
		
		// Console에 출력하기 위한 
		// println() method 
		System.out.println();
		
	}
}
